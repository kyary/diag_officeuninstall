#*=================================================================================
# Load Utilities
#*=================================================================================
. .\CL_OfficeUninstall.ps1
#*=================================================================================
#Import Localized data for strings
#*=================================================================================
Import-LocalizedData -BindingVariable Strings_TS_Main -FileName TS_Main
Write-DiagProgress -activity $strings_TS_Main.ID_Name_RC_Status

# Get the architecure of a system
$systemArchitectureObj = Get-WMIObject -Class Win32_Processor
$systemArchitecture = $systemArchitectureObj.Architecture
$installedName = $strings_TS_Main.id_name_progst_installed
#If the device is ARM then block the pack
if($systemArchitecture -eq 5)
{
	Get-Diaginput -id "INT_ARMDevice"
}
else
{
# Returns the object contains installed office and installed date.
$officeList = Get-InstalledOffice
[String]$officeVersionName = $officeList.OfficeID
$measure = $officeList | Measure 
$count = $measure.Count
#Call NoOffice Rootcause if count is 0
   if($count -eq 0)
   {
      $rcName = $strings_TS_Main.ID_NAME_RS_NOffice	
	  $rcDescription = $strings_TS_Main.ID_DESC_RS_NOffice	
	  . ./RC_NoOffice.ps1 $rcName $rcDescription $count
   }
   #Call Single office rootcause if count eq 1
   elseif($count -eq 1)
   {
	  # check which office need to be uninstalled.
	   Switch ($officeVersionName)
	   {
		 "Office11"
		 {
			 $rcName = "Microsoft Office 2003 $installedName"
		 }
		 "Office12"
		 {
			 $rcName = "Microsoft Office 2007 $installedName"
		 }
		 "Office14"
		 {
			 $rcName = "Microsoft Office 2010 $installedName"
		 }
		 "Office15"
		 {
			 $rcName = "Microsoft Office 2013 MSI $installedName"
		 }
		 "Office16"
		 {
		     $rcName = "Microsoft Office 2016 MSI $installedName"
		 }
		 "Office16c2r"
		 {
			$rcName = "Microsoft Office 2016 Click-to-Run $installedName"
		 }
		 "Office15c2r"
		 {
			$rcName = "Microsoft Office 2013 Click-to-Run $installedName"
		 }
	   }
	  $rcDescription = $strings_TS_Main.ID_NAME_DIAG_Off15C2R
	  . ./RC_SingleOffice.ps1 $rcName $rcDescription $officeVersionName $count
   }
   #Call Multiple office rootcause if count is greater than 1
   elseif($count -gt 1)
   {
	   	#Get the final office ID if multiple office installed...
	   $lastInstalledOfffce = $officeList | Sort-Object -Property InstalledDate -descending | Select -First 1 
	   $rcName = $strings_TS_Main.ID_NAME_RS_MOffice	
	   $rcDescription = $strings_TS_Main.ID_DESC_RS_MOffice	
	   $lastinstalledofficeID = $lastInstalledOfffce.OfficeID
		. ./RC_MultipleOffice.ps1 $rcName $rcDescription $lastinstalledofficeID $count 
   }   
}

